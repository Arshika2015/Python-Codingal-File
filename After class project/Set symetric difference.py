set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}
symmetric_diff_method = set1.symmetric_difference(set2)
print(f"Symmetric difference using symmetric_difference() method: {symmetric_diff_method}")
symmetric_diff_operator = set1 ^ set2
print(f"Symmetric difference using ^ operator: {symmetric_diff_operator}")
